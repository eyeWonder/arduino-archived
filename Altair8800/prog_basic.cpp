uint16_t prog_basic_copy_4k(byte *dst)
{
  host_copy_flash_to_ram(dst, basic4k, 0x1000);

#if MEMSIZE>=0x10000
  // if we have 64k ram, the automatic memory detection in 4k BASIC will
  // fail, resulting in an infinite loop => patch BASIC to skip memory detection
  dst[0xDCB] = 0xFE;
  dst[0xDCC] = 0xFF;
  dst[0xDD2] = 0xC3;
#endif  
  
  return 0x0;
}


uint16_t prog_basic_copy_16k(byte *dst)
{
  host_copy_flash_to_ram(dst+0xC000, basic16k, 0x4000);
  return 0xC000;
}


byte prog_basic_read_16k(uint16_t addr)
{
  return pgm_read_byte(basic16k + (addr & 0x3fff));
}
